		std::pair<int, int> edge = _edgesByIndex[ie];
		if (edge.first == edge.second) {
			int ip = indexOfPoint(points, _points[edge.first]);
			if (ip != -1) {
				counts[ip] = 3;
				continue;
			}
		}

		v3f_t fst = _points[edge.first];
		v3f_t scnd = _points[edge.second];
