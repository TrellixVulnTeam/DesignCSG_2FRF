int tcount(int* indices) {
	int nTriangles = 0;
	for (int i = 0; i < 5; i++) {
		if (indices[i * 3] != -1) nTriangles++;
	}
	return nTriangles;
}

void edgeOccupancy(int lookup, int* edgeOccupancyTable) {
	int* trs = triTable[lookup];
	int tc = tcount(trs);
	for (int it = 0; it < tc; it++) {
		if (trs[it * 3 + 0] >= 0) edgeOccupancyTable[trs[it * 3 + 0]] = 1;
		if (trs[it * 3 + 1] >= 0) edgeOccupancyTable[trs[it * 3 + 1]] = 1;
		if (trs[it * 3 + 1] >= 0) edgeOccupancyTable[trs[it * 3 + 2]] = 1;
	}
}

