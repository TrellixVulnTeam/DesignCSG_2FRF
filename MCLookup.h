#define faceCheck(arg,A,B,C,D) {if (arg==A) return 1; if (arg==B) return 1; if (arg==C) return 1; if (arg==D) return 1;} 
int edgeIsOnFace(int edge, FaceType faceType) {
	switch (faceType) {
	case FaceType::East: faceCheck(edge, 1, 5, 9, 10);  break;
	case FaceType::West: faceCheck(edge, 3, 7, 8, 11);  break;
	case FaceType::North: faceCheck(edge, 0, 4, 8, 9);  break;
	case FaceType::South: faceCheck(edge, 2, 6, 10, 11);  break;
	case FaceType::Above: faceCheck(edge, 4, 5, 6, 7);  break;
	case FaceType::Below: faceCheck(edge, 0, 1, 2, 3);  break;
	}
	return 0;
}

FaceType getFaceType(JunctionType junctionType, int primaryOrSecondary) {
	switch (junctionType) {

	case JunctionType::EastWest:  return (primaryOrSecondary == SECONDARY) ? FaceType::West : FaceType::East; break;
	case JunctionType::NorthSouth:  return (primaryOrSecondary == SECONDARY) ? FaceType::South : FaceType::North; break;
	case JunctionType::AboveBelow:  return (primaryOrSecondary == SECONDARY) ? FaceType::Below : FaceType::Above; break;
	}


	return FaceType::East;
}

int cornerIsOnFace(FaceType faceType, int c) {
#define memberOf(A,B,C,D,X) (X==A||X==B||X==C||X==D)
	switch (faceType) {
	case FaceType::East: return memberOf(1, 2, 5, 6, c); break;
	case FaceType::West: return memberOf(0, 3, 4, 7, c); break;
	case FaceType::North: return memberOf(0, 1, 4, 5, c); break;
	case FaceType::South: return memberOf(2, 3, 6, 7, c); break;
	case FaceType::Below: return memberOf(0, 1, 2, 3, c); break;
	case FaceType::Above: return memberOf(4, 5, 6, 7, c); break;
	}
	return 0;

}

